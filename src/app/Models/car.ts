export class Car{
    name: string;
    year: string;
    seats: string;
    transmission: string;
    description: string;
    image: string;
    constructor(name: string, year: string, seats: string, transmission: string, description: string, image: string){
        this.name=name;
        this.year = year;
        this.seats = seats;
        this.transmission = transmission;
        this.description = description;
        this.image = image;
    }
}


  